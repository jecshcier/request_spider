function saveFiles(t, data) {
	data.currentNum++;
	let gen = t.next()
	if (gen.done) {
		return
	}
	let re = gen.value
	let pos = path.extname(re.url)
	let writeStream = fs.createWriteStream(data.dirName + '/' + fileList[data.i].items[data.currentNum].fileName + pos);
	re.pipe(writeStream)
	writeStream.on('finish', () => {
		console.log(fileList[data.i].items[data.currentNum].fileName + pos + '下载完成！')
		saveFiles(t, data)
	})
}

function get_req(url, data, callback) {
	request // 发起请求
		.get(url)
		.set('User-Agent', 'Mozilla/5.0 (Macintosh; Intel Mac OS X 10_12_6) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/67.0.3396.87 Safari/537.36')
		.timeout({
			response: 5000, // Wait 5 seconds for the server to start sending,
			deadline: 20000, // but allow 1 minute for the file to finish loading.
		})
		.end((err, respons) => {
			if (err) {
				if (!data.count) {
					data.count = 1
				} else {
					data.count++;
				}
				if (data.count < 3) {
					get_req(url, data, callback)
				} else {
					console.log(url + '出错3次仍然失败----------------------->')
					console.log(err)
					callback()
				}
			} else {
				let resData = {}
				try {
					resData = JSON.parse(respons.text)
				} catch (e) {
					console.log(e)
				}
				fileList[data.chapter].items[data.fileName].url = resData.url
				callback()
			}
		})
}

